// switch statements

// syntax

// generates number from 1 to 7
// if 7 is generated, then there is no match
switch(Math.floor(Math.random() * 7 + 1)){
    case 1:
        console.log(1);
        break;
    case 2:
        console.log(2);
        break;
    case 3:
        console.log(3);
        break;
    case 4:
        console.log(4);
        break;
    case 5:
        console.log(5);
        break;
    case 6:
        console.log(6);
        break;
    default:
        console.log('no match');
}
